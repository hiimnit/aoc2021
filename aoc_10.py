PART1_SCORE_TABLE = {
    ')': 3,
    ']': 57,
    '}': 1197,
    '>': 25137,
}

PART2_SCORE_TABLE = {
    ')': 1,
    ']': 2,
    '}': 3,
    '>': 4,
}

CHUNK_MAP = {
    '(': ')',
    '[': ']',
    '{': '}',
    '<': '>',
}

def is_corrupted(line) -> tuple[bool, str]:
    stack = []

    for c in line:
        if c in ['(', '[', '{', '<']:
            stack.append(CHUNK_MAP[c])
        else:
            if c != stack[-1]:
                return True, c
            stack.pop()

    return False, stack

if __name__ == '__main__':
    # part 1
    result = 0

    for line in input:
        corrupted, c = is_corrupted(line)
        if corrupted:
            result += PART1_SCORE_TABLE[c]

    print('result=', result)

    # part 2
    incomplete_lines = [line for line in input if is_corrupted(line) == (False, None)]
    scores = []
    
    for line in input:
        corrupted, missing_chars = is_corrupted(line)
        if not corrupted:
            score = 0
            for char in reversed(missing_chars):
                score = score * 5 + PART2_SCORE_TABLE[char]
            scores.append(score)

    scores.sort()
    result = scores[floor(len(scores) / 2)]

    print('result=', result)
